const io = require("socket.io-client");

const socket = io("http://localhost:5000");
socket.on("connect", () => console.log("Connected"));
socket.on("initial_data", (data) => console.log("Initial data:", data));
socket.on("price_update", (data) => console.log("Price update:", data));
socket.on("trade_executed", (data) => console.log("Trade executed:", data));
socket.on("trade_closed", (data) => console.log("Trade closed:", data));
socket.on("error", (err) => console.error("Error:", err));
socket.on("disconnect", () => console.log("Disconnected"));
socket.on("reconnect", () => console.log("Reconnected"));
socket.on("reconnect_attempt", () => console.log("Reconnect attempt"));
socket.on("reconnecting", () => console.log("Reconnecting"));
socket.on("reconnect_error", (err) => console.error("Reconnect error:", err));
socket.on("reconnect_failed", () => console.error("Reconnect failed"));
socket.on("ping", () => console.log("Ping"));
socket.on("pong", () => console.log("Pong"));
socket.on("connect_error", (err) => console.error("Connect error:", err));
socket.on("connect_timeout", () => console.error("Connect timeout"));
socket.on("connect_failed", () => console.error("Connect failed"));
socket.on("disconnecting", () => console.log("Disconnecting"));
socket.on("new_listener", (event) => console.log("New listener:", event));
socket.on("remove_listener", (event) => console.log("Remove listener:", event));
socket.on("ping_timeout", () => console.error("Ping timeout"));
socket.on("pong_timeout", () => console.error("Pong timeout"));
socket.on("upgrade", () => console.log("Upgrade"));
socket.on("upgrade_error", (err) => console.error("Upgrade error:", err));
socket.on("packet", (packet) => console.log("Packet:", packet));
socket.on("packetCreate", (packet) => console.log("Packet create:", packet));
